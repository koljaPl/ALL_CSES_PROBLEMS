# Выбор структуры данных в Python критичен для скорости:

# list.pop(0) — это O(N). Если нужна очередь, используй collections.deque (там popleft() это O(1)).

# x in list — это O(N). Если нужно часто проверять наличие элемента, используй set (множество) — там это O(1).

